    <p>
       The nineteenth century saw an improvement in conditions for Irish Catholics and this revival is best reflected in the wave of chapel building which swept the country. In 1815 the foundation was laid for the magnificent Pro Cathedral,in Marlborough Street. In Coolock, too, the revival saw the erection of a new thatched chapel in 1820. Eleven years later,this was replaced by a larger chapel, (the foundation stone of which is now in the garden of the Community Centre). It would be wrong, however to believe that the church in Coolock was well established. In many respects, conditions in Coolock resembled a mission station. A report to Archbishop Murray in 1831 indicated that the new chapel was incomplete because of lack of funds. Before long, however it was completed. Lewis's Topographical Directory of Ireland, published 1837, contains a description the chapel: 'erected in 1831, at an expense of ï¿½800, raised by subscription, it is a very neat edifice, in the later English style, with a belfry over the principal entrance; the interior is very well arranged and neatly decorated.' Still, the report to Dr Murray in 1831 indicated that there was only one Mass on Sunday 'when the chapel was open' and there were no confirmations in the parish. There was a parish priest and a curate, but they relied on the support of lay people who taught catechism to the children in the chapel each Sunday morning.<br /><br />

This was common practice in the archdiocese, where priests relied upon the assistance of the Confraternity of the Christian Doctrine to evangelise the people. Significantly, too, the Archbishop requested the parish priest to forward statistical information about the parish. He was 'to deliver in writing the names of the most obstinate absentees,of public sinners in the parish, with some account of their crimes, and of public abuses, such as illegal combinations, drunkenness, quarreling, violations of the Lord's Day, Night-Wakes, or Public Dances. As may deserve to be noticed by the Archbishop'. While such questionnaires were sent to all pastors of the diocese, many chose to supply the Archbishop with information that exaggerated the health of the parish. In this instance, any such report would have been futile since the archbishop, Daniel Murray, had served briefly in Coolock as parish priest himself. Sometimes referred to as 'the Archbishop of the reconstruction' Dr. Murray is best remembered for his efforts to encourage religious life within the diocese and his attempts to advance Catholic education. In both cases,a Coolock woman, Catherine McAuley (1778-1841) was particularly active.

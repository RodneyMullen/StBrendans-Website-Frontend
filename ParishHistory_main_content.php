    <p>
        By the 1860s, it was necessary once more to replace the parish church and the Catholic Directory (1864) carried an account of the dedication of the new chapel which recalled the extent to which the Catholic church had advanced since the penal era: 'The ceremony of solemnly opening the beautiful rural church of St. Brendan, Coolock, County Dublin, after the completion of the enlargements and decorations which have recently been executed through the zeal and charity of the esteemed administrator, Rev. Michael Brady and the faithful people of the district. Within the memory of many, the place at which the rites of the Church at Coolock had to be celebrated was a mud cabin, scarcely competent to afford shelter from the weather. The present church, built in the form of a cross, is a neat and commodious edifice exteriorly and its interior is finished in a style of taste and elegance which reflect the greatest credit to the good priest and his flock'.

    <p>This effort to preserve the past also accounts for the curious presence in the church of not one but three tabernacles. The continuity is also reflected in the beautiful stained glass window which is now in the baptistery, built in the 1990s, but which originally graced the old chapel. Depicting the apparition of the Sacred Heart to St. Margaret Mary Alacoque (1647-90), the window is the work of the Earley family, the renowned stained glass artists of Camden Street. A gift of Mr Gilbey, the wine merchant, the window has a significant local connection, in that the model for St. Margaret Mary Alacoque was a local girl, Alacoque Taylor of Beechpark who had been called after the saint.

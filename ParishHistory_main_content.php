     <p>
         The 1990s brought further changes to the parish. In 1990, Canon Patrick O'Farrell went to his reward. Much loved by the people of Coolock, Fr O'Farrell was famous not only for his enthralling sermons, which were continued from Sunday to Sunday and usually contained entertaining updated accounts of biblical stories. Perhaps even more famous was the holy man's faithful dog, Skippy who was the best loved dog in the world. When Canon O'Farrell went on school visits , Skippy was given the teacher's chair, where he sat patiently listening to the story telling. Then as soon as he heard the words "and they all lived happily ever after" Skippy headed for the door in the sure knowledge that his time in the classroom was over. Fr O'Farrell's passing marked the end of an era and within a few years the Archbishop entrusted the parish to the care of the Marists.

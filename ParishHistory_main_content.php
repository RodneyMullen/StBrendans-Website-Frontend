    <p>
        This church remained in use for over a century, but during that time is was modified on several occasions. In 1916 a bell tower was added at a cost of ï¿½114. Three times each day the bell was rung manually to announce the Angelus, until the tower was removed in the 1970s.

By that time the old church had fallen into disrepair, but it was also unsuited to the liturgical reforms of the Second Vatican Council. For that reason, the Parish Priest Father Mansfield launched an ambitious campaign to replace it. Through their generosity and energetic fundraising, the people of Coolock raised ï¿½80,000.The fundraising campaign included planned giving, cake sales, bingo and many more events. Many individuals and groups donated seats, shrines and vestments in order to build and fit out the present church, which was dedicated to St Brendan by Archbishop Dermot Ryan in July 1974. Yet conscious of the tradition of worship on the site, efforts were made to save as much of the old church as possible and it has subsequently been cleverly converted into a parish centre of which the community can be exceptionally proud. </p><br /><br />
